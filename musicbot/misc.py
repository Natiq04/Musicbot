class Zone(tzinfo):
    def __init__(self, offset, isdst, name):
        self.offset = offset
        self.isdst = isdst
        self.name = name

    def utcoffset(self, dt):
        return timedelta(hours=self.offset) + self.dst(dt)

    def dst(self, dt):
        return timedelta(hours=1) if self.isdst else timedelta(0)

    def tzname(self, dt):
        return self.name


GMT = Zone(+4, False, "GMT")


async def calculate_hours(number) -> List[datetime]:
    return [
        datetime.now(GMT).replace(hour=0, minute=0, second=0, microsecond=0)
        + timedelta(hours=number * i)
        for i in range(int(24 / number) + 1 if 24 % number != 0 else int(24 / number))
    ]
